package no.cheatsheet.basics.namingconventions;

public class namingConvention {
    // Packages
    //  com, edu, gov, mil, net, org
    //  or
    //  country code, e.g. no, gb, us
    //  com.sun.eng - no.hvl.dat152

    //Classes
    //  Class names should be nouns, first letter of each word capitalized. Simple and descriptive
    //  ImageSprite, ClassDelegator, CountryGet, CLass

    //Interfaces
    //  Similar to class names.
    //  ThisInterface, Interface

    //Methods
    //  Methods should be verbs, first letter lowercase, next internal words capitalized on first letter.
    // thisMethod, thatMethod, method

    //Variables
    //  Same as methods, lowerCase
    //  Should be short and meaningful

    //Constants
    //  Constants are declared by capitalized words separated by underscores _
    //  THIS_CONSTANT = X; THIS_IS_MAX = Y;
}
